package help;

public class Helper {

    public static int maximum(int a,int b) {
        if (a > b) {
            return a;
        } else {
            return b;
        }
    }
    public static int minimum(int a,int b) {
        if(a<b) {
            return a;
        }
        else {
            return b;
        }
    }
}
